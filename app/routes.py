from flask import render_template, flash, redirect, url_for, request, current_app as app
from app import db
from app.forms import LoginForm, RegistrationForm, PostForm
from app.models import User, Post
from flask_login import current_user, login_user, logout_user, login_required
from urllib.parse import urlparse, urljoin  # Correct import
from app import REQUEST_TIME, REQUEST_COUNTER
from prometheus_client import generate_latest
from flask import Response


@app.route('/')
@app.route('/index')
@REQUEST_TIME.time()
def index():
    REQUEST_COUNTER.inc()
    posts = Post.query.order_by(Post.timestamp.des()).all()
    return render_template('home.html', posts=posts)

@app.route('/metrics')
def metrics():
    return Response(generate_latest(), mimetype='text/plain')

@app.route('/login', methods=['GET', 'POST'])
def login():
    if current_user.is_authenticated:
        return redirect(url_for('index'))
    form = LoginForm()
    if form.validate_on_submit():
        user = User.query.filter_by(username=form.username.data).first()
        if user is None or not user.check_password(form.password.data):
            flash('Invalid username or password')
            return redirect(url_for('login'))
        login_user(user, remember=form.remember_me.data)
        next_page = request.args.get('next')
        if not next_page or urlparse(next_page).netloc != '':
            next_page = url_for('index')
        return redirect(next_page)
    return render_template('login.html', title='Sign In', form=form)

@app.route('/logout')
def logout():
    logout_user()
    return redirect(url_for('index'))

@app.route('/register', methods=['GET', 'POST'])
def register():
    if current_user.is_authenticated:
        return redirect(url_for('index'))
    form = RegistrationForm()
    if form.validate_on_submit():
        user = User(username=form.username.data, email=form.email.data)
        user.set_password(form.password.data)
        db.session.add(user)
        db.session.commit()
        flash('Congratulations, you are now a registered user!')
        return redirect(url_for('login'))
    return render_template('register.html', title='Register', form=form)

@app.route('/post/new', methods=['GET', 'POST'])
@login_required
def new_post():
    form = PostForm()
    if form.validate_on_submit():
        post = Post(title=form.title.data, body=form.body.data, author=current_user)
        db.session.add(post)
        db.session.commit()
        flash('Your post is now live!')
        return redirect(url_for('index'))
    return render_template('new_post.html', title='New Post', form=form)

@app.route('/post/<int:id>')
def post(id):
    post = Post.query.get_or_404(id)
    return render_template('post.html', post=post)

@app.route('/admin/users')
@login_required
def list_users():
    if not current_user.is_admin:  # Assuming you have a way to check for admin privileges
        flash('You do not have permission to view this page.')
        return redirect(url_for('index'))
    
    users = User.query.all()
    return render_template('list_users.html', users=users)
